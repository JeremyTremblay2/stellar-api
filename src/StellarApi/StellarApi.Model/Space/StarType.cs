    /// </summary>
    RedGiant,
    /// <summary>
    /// Red supergiant star are the largest stars in the universe.
    /// </summary>
    RedSuperGiant,
    /// <summary>
    /// White dwarf star are small and dense stars.
    /// </summary>
    WhiteDwarf,
    /// <summary>
    /// Red dwarf star are small and cool stars.
    /// </summary>
    RedDwarf,
    /// <summary>
    /// Blue giant star are bright and large stars.
    /// </summary>
    BlueGiant,
    /// <summary>
    /// Yellow dwarf star are small and cool stars.
    /// </summary>
    YellowDwarf,
    /// <summary>
    /// Neutron star are the smallest and densest stars.
    /// </summary>
    NeutronStar,
    /// <summary>
    /// Bl
    /// </summary>
    BlackHole,
    /// <summary>
    /// An unknown star type.
    /// </summary>
